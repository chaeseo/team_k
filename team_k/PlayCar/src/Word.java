
public class Word {//¿À¸ñµ¹
	public int x;
	public int y;
	public int color;
	
	public Word(int x, int y, int color) {
		this.x = x;
		this.y = y;
		this.color = color;
	}
	
	public int GetX() {
		return x;
	}
	
	public int GetY() {
		return y;
	}
	
	public int GetColor() {
		return color;
	}
	
	public void SetX(int x) {
		this.x = x;
	}
	
	public void SetY(int y) {
		this.y = y;
	}
	
	public void SetColor(int color) {
		this.color = color;
	}

}
